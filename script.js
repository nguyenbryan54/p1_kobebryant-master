$kobe1.waypoint(function(direction) {
  if (direction == 'down'){
    $kobe1.attr('src', 'https://media.giphy.com/media/WlznRh8sLYgE/giphy.gif');
  } else {
    $kobe1.attr('src', 'https://i.insider.com/5c0aeba64d340b27cc0c0f0d?width=300')
  }
}, { offset: '25%'});



